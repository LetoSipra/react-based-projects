function App() {
  const [power, setPower] = useState(true);
  const [sound, setSound] = useState(soundData1);
  const [soundID, setSoundID] = useState("Display");
  const [volume, setVolume] = useState(0.5);

  const playSound = (id) => {
    if (power) {
      const sound = document.getElementById(id);
      sound.volume = volume;
      sound.play();
    }
  };

  const checkPower = () => {
    power ? setPower(false) : setPower(true);
  };

  const checkSoundData = () => {
    sound === soundData1 ? setSound(soundData2) : setSound(soundData1);
  };

  const detectKeyDow = (e) => {
    sound.map((getSound) => {
      if (e.keyCode == getSound.keyCode) {
        playSound(getSound.key);
        setSoundID(getSound.id);
      }
    });
  };

  const display = () => {
    if (power) {
      return (
        <>
          <h1>ID:{soundID}</h1>
          <h2>Volume:{Math.round(volume * 100)}</h2>
        </>
      );
    }
  };

  useEffect(() => {
    document.addEventListener("keydown", detectKeyDow, true);
    return () => {
      document.removeEventListener("keydown", detectKeyDow, true);
    };
  });

  return (
    <main className='flex h-screen bg-slate-500' id='app'>
      <div className='relative m-auto'>
        <div className='grid grid-cols-3 gap-5' id='drum-machine'>
          {sound.map((getSound) => {
            return (
              <div className=''>
                <button
                  className='drum-pad inline-block rounded bg-neutral-50 px-6 pt-2.5 pb-2 text-xs font-medium uppercase leading-normal text-neutral-800 shadow-[0_4px_9px_-4px_#fbfbfb] transition duration-150 ease-in-out hover:bg-neutral-100 hover:shadow-[0_8px_9px_-4px_rgba(251,251,251,0.3),0_4px_18px_0_rgba(251,251,251,0.2)] focus:bg-neutral-100 focus:shadow-[0_8px_9px_-4px_rgba(251,251,251,0.3),0_4px_18px_0_rgba(251,251,251,0.2)] focus:outline-none focus:ring-0 active:bg-neutral-200 active:shadow-[0_8px_9px_-4px_rgba(251,251,251,0.3),0_4px_18px_0_rgba(251,251,251,0.2)]'
                  id={getSound.id}
                  onClick={() => {
                    playSound(getSound.key);
                    setSoundID(getSound.id);
                  }}>
                  <audio
                    src={getSound.url}
                    className='clip'
                    id={getSound.key}></audio>
                  {getSound.key}
                </button>
              </div>
            );
          })}
        </div>
        <div className=''>
          <div className='text-center '>
            <input
              className='mt-5'
              type='range'
              min={0}
              max={1}
              step={0.02}
              value={volume}
              onChange={(event) => {
                setVolume(event.target.valueAsNumber);
              }}
            />
          </div>
          <div className='text-center'>
            <button
              className='m-3 mt-5 ml-3 mb-3 inline-block rounded bg-neutral-800 px-6 pt-2.5 pb-2 text-xs font-medium uppercase leading-normal text-neutral-50 shadow-[0_4px_9px_-4px_#332d2d] transition duration-150 ease-in-out hover:bg-neutral-800 hover:shadow-[0_8px_9px_-4px_rgba(51,45,45,0.3),0_4px_18px_0_rgba(51,45,45,0.2)] focus:bg-neutral-800 focus:shadow-[0_8px_9px_-4px_rgba(51,45,45,0.3),0_4px_18px_0_rgba(51,45,45,0.2)] focus:outline-none focus:ring-0 active:bg-neutral-900 active:shadow-[0_8px_9px_-4px_rgba(51,45,45,0.3),0_4px_18px_0_rgba(51,45,45,0.2)] dark:bg-neutral-900 dark:shadow-[0_4px_9px_-4px_#171717] dark:hover:bg-neutral-900 dark:hover:shadow-[0_8px_9px_-4px_rgba(27,27,27,0.3),0_4px_18px_0_rgba(27,27,27,0.2)] dark:focus:bg-neutral-900 dark:focus:shadow-[0_8px_9px_-4px_rgba(27,27,27,0.3),0_4px_18px_0_rgba(27,27,27,0.2)] dark:active:bg-neutral-900 dark:active:shadow-[0_8px_9px_-4px_rgba(27,27,27,0.3),0_4px_18px_0_rgba(27,27,27,0.2)]'
              onClick={checkPower}>
              ON/OFF
            </button>
            <button
              className='mt-5 ml-3 mb-3 inline-block rounded bg-neutral-800 px-6 pt-2.5 pb-2 text-xs font-medium uppercase leading-normal text-neutral-50 shadow-[0_4px_9px_-4px_#332d2d] transition duration-150 ease-in-out hover:bg-neutral-800 hover:shadow-[0_8px_9px_-4px_rgba(51,45,45,0.3),0_4px_18px_0_rgba(51,45,45,0.2)] focus:bg-neutral-800 focus:shadow-[0_8px_9px_-4px_rgba(51,45,45,0.3),0_4px_18px_0_rgba(51,45,45,0.2)] focus:outline-none focus:ring-0 active:bg-neutral-900 active:shadow-[0_8px_9px_-4px_rgba(51,45,45,0.3),0_4px_18px_0_rgba(51,45,45,0.2)] dark:bg-neutral-900 dark:shadow-[0_4px_9px_-4px_#171717] dark:hover:bg-neutral-900 dark:hover:shadow-[0_8px_9px_-4px_rgba(27,27,27,0.3),0_4px_18px_0_rgba(27,27,27,0.2)] dark:focus:bg-neutral-900 dark:focus:shadow-[0_8px_9px_-4px_rgba(27,27,27,0.3),0_4px_18px_0_rgba(27,27,27,0.2)] dark:active:bg-neutral-900 dark:active:shadow-[0_8px_9px_-4px_rgba(27,27,27,0.3),0_4px_18px_0_rgba(27,27,27,0.2)]'
              onClick={checkSoundData}>
              CHANGE KIT
            </button>
            <div id='display' className='h-1 w-auto text-3xl'>
              {display()}
            </div>
          </div>
        </div>
      </div>
    </main>
  );
}

export default App;
